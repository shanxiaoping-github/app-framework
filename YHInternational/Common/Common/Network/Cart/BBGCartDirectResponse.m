#import "BBGCartDirectResponse.h"

@implementation BBGCartDirectResponse

- (id)initWithData:(NSData *)data format:(ResponseDataFormat)format{
    self = [super initWithData:data format:format];
    if (self) {
        if (self.isError) {
            return self;
        }
    }
    return self;
}

@end
