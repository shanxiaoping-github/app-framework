#import "BBGSpecHeader.h"

@implementation BBGSpecHeader

- (id)initWithFrame:(CGRect)frame {
    self = [super initWithFrame:frame];
    if (self) {
        _titleLab = [[UILabel alloc]init];
        _titleLab.frame = CGRectMake(10, 0, self.frame.size.width, self.frame.size.height);
        _titleLab.textAlignment = NSTextAlignmentLeft;
        _titleLab.textColor = UICOLOR_FONT_GENERAL_GRAY;
        _titleLab.font = APP_FONT_DEMI_LIGHT(15.0f);
        [self addSubview:_titleLab];
    }
    return self;
}

@end
