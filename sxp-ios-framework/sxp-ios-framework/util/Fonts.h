#ifndef _Fonts_h
#define _Fonts_h
/*获取字体*/
#define fontWith(f) [UIFont systemFontOfSize:(f)]
/*自定义字体*/
/*广告字体*/
#define advertising_font fontWith(25.f)
/*标题字体*/
#define title_font fontWith(20.f)
/*标准字体*/
#define normal_font fontWith(17.f)
/*中等字体*/
#define middle_font fontWith(15.f)
/*小字体*/
#define small_font fontWith(13.f)
/*最小字体*/
#define last_small_font fontWith(11.f)
#endif
